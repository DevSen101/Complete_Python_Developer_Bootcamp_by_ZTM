# Dunder methods (double underscore or "magic methods") are special methods in Python classes with names like __init__ or __str__, enabling operator overloading and built-in behaviors.

# Common Examples
# __init__(self): Constructor, initializes object on creation

# __str__(self): String representation (for print())

# __repr__(self): Developer-friendly string (for debugging)

# __len__(self): Length for len() calls

# __add__(self, other): + operator (e.g., obj1 + obj2)

# __eq__(self, other): == comparison